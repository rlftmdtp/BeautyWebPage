package ch11.logon;

public class CosmeticDataBean {
	private String contents;
	private String title;
	private String beautytype;
	private String age;
	private String grade;
	private String contetns;
	private String productname;
	private String image;
	
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getBeautytype() {
		return beautytype;
	}
	public void setBeautytype(String beautytype) {
		this.beautytype = beautytype;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public String getContetns() {
		return contetns;
	}
	public void setContetns(String contetns) {
		this.contetns = contetns;
	}
	public String getProductname() {
		return productname;
	}
	public void setProductname(String productname) {
		this.productname = productname;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	
	
}
